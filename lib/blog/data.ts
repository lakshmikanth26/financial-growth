export function getBlogPostsByCategory(category: string): BlogPost[] {
  return Object.values(blogPosts).filter(post => post.category === category);
}

export function getBlogPostsByTag(tag: string): BlogPost[] {
  return Object.values(blogPosts).filter(post => post.tags.includes(tag));
}

export function getRelatedBlogPosts(currentSlug: string, limit: number = 3): BlogPost[] {
  const currentPost = getBlogPost(currentSlug);
  if (!currentPost) return [];

  const relatedPosts = Object.values(blogPosts)
    .filter(post => 
      post.slug !== currentSlug && 
      (post.category === currentPost.category || 
       post.tags.some(tag => currentPost.tags.includes(tag)))
    )
    .slice(0, limit);

  return relatedPosts;
}